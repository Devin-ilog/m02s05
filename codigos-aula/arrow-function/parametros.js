//quando há apenas um parâmetro
function umaFuncao(umParametro) {
    console.log(umParametro);
}

umParametro => {
    console.log(umParametro);
}

//quando há dois ou mais parâmetros
function umaFuncaoQualquer(parametroUm, parametroDois) {
    console.log(parametroUm);
    console.log(parametroDois);
}

//Quando há dois ou mais parâmetros os parênteses são obrigatórios
(parametroUm, parametroDois) => {
    console.log(parametroUm);
    console.log(parametroDois);
}